      printf("是否考虑右侧第三项(Y/N):");
      scanf(" %c", &jud);
                             //设置输入参数
      
      printf("\nN           lnN!              Approve           differ           1/RE_dif          1/N_re_dif\n");
      printf("___________________________________________________________________________________________\n\n"); 
                              //表头
 
      dis = n / point;  //计算取点间隔
 
      for(int i=0; i<=n; i+=dis)
        {
          if(i==0)
            {
              left = 0;
              right =0;
              differ = 0;
              re_dif = 1;
            }
          else
            {
               left = 0.00; //初始化方程左侧

               for(int j=1; j<=i; j++)
                  left += log(j);   //计算lnN! 
           
               if('Y' == jud || 'y' == jud) 
                  right = (i * log(i)) - i + (0.500 * log(2 * M_PI * i));
               else
                  right =(i * log(i)) - i;    //计算方程右侧

               differ = left - right; // 计算左右差值
           
               re_dif = left / differ; // 计算相对误差的倒数
           
               N_re_dif = 1 / (1-exp(-differ)); //计算去掉ln后的相对误差的倒数

            }      
           
           printf("\n\n%d         %lf            %lf          %lf           %lf            %lf", i, left, right, differ, re_dif, N_re_dif);                    
        
        }

      printf("\n\n_________________________________________________________________________________________\n\n");
      printf("Program Complete!!\n\n"); 
